function getPokerScore(id)
	local value = getPokerValue(id)
	if value == 5 then
		return 5
	elseif value == 13 or value == 10 then
		return 10
	else
		return 0
	end
end

function getPokerValue(id)
	return poker_data[id].value
end

function getPokersValueEqual(ids)
	for i,val in pairs(ids) do
		if i > 1 and poker_data[val].value ~= poker_data[ids[i-1]].value then
			return false
		end
	end
	return true
end

function getPokerWeg(id)
